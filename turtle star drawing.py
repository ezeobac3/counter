import turtle
for i in range(60):
    turtle.forward(100)
    turtle.color("blue")
    turtle.right(200)
    turtle.forward(150)
    turtle.right(230)
    turtle.forward(120)
    turtle.color("red")
    turtle.color("purple")
    turtle.bgcolor("green")

for i in range(60):
    turtle.backward(150)
    turtle.color("deep blue")
    turtle.right(200)
    turtle.backward(150)
    turtle.right(230)
    turtle.backward(120)
    turtle.color("purple")
    turtle.color("lemon green")
    turtle.bgcolor("white")




